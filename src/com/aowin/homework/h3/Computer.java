package com.aowin.homework.h3;

public class Computer {
	private Key k;
	private Screen s;
	private Mouse m;
	private Time t;
	public Key getK() {
		return k;
	}
	public void setK(Key k) {
		this.k = k;
	}
	public Screen getS() {
		return s;
	}
	public void setS(Screen s) {
		this.s = s;
	}
	public Mouse getM() {
		return m;
	}
	public void setM(Mouse m) {
		this.m = m;
	}
	public Time getT() {
		return t;
	}
	public void setT(Time t) {
		this.t = t;
	}
	
}
